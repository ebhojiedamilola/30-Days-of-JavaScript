function areaOfRectangle(length, width) {
  return length * width;
}
console.log(areaOfRectangle(10, 5));

//5
function perimeterOfRectangle(length, width) {
  return 2 * (length + width);
}
console.log(perimeterOfRectangle(10, 5));

